-- Enable RLS
ALTER TABLE public.parcel_detail ENABLE ROW LEVEL SECURITY;

ALTER TABLE public.parcel_deeds ENABLE ROW LEVEL SECURITY;

-- RLS policies for parcel_detail
CREATE POLICY parcel_detail_select_policy ON public.parcel_detail FOR
SELECT
    TO authenticated USING (
        auth.role () = 'service_role'
        OR public.authorize ('parcel_detail.read')
    );

CREATE POLICY parcel_detail_insert_policy ON public.parcel_detail FOR INSERT TO authenticated
WITH
    CHECK (
        auth.role () = 'service_role'
        OR public.authorize ('parcel_detail.write')
    );

CREATE POLICY parcel_detail_update_policy ON public.parcel_detail FOR
UPDATE TO authenticated USING (
    auth.role () = 'service_role'
    OR public.authorize ('parcel_detail.write')
)
WITH
    CHECK (
        auth.role () = 'service_role'
        OR public.authorize ('parcel_detail.write')
    );

CREATE POLICY parcel_detail_delete_policy ON public.parcel_detail FOR DELETE TO authenticated USING (
    auth.role () = 'service_role'
    OR public.authorize ('parcel_detail.delete')
);

