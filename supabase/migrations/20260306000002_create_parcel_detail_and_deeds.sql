-- Create parcel_detail and parcel_deeds tables with permissions and RLS policies
CREATE TABLE
    IF NOT EXISTS public.parcel_detail (
        id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        parcel_id bigint NOT NULL,
        legal_id text,
        legal_desc text,
        tax_status text,
        tax_code text,
        use_code text,
        occupancy text,
        class_code text,
        nbhd_group text,
        ward text,
        cda text,
        first_year integer NOT NULL,
        last_year integer NOT NULL,
        created_at timestamptz NOT NULL DEFAULT now (),
        updated_at timestamptz NOT NULL DEFAULT now (),
        CONSTRAINT parcel_detail_year_range_check CHECK (first_year <= last_year)
    );

COMMENT ON TABLE public.parcel_detail IS 'Parcel detail history grouped by parcel attributes with first/last year coverage.';

CREATE INDEX IF NOT EXISTS parcel_detail_parcel_id_idx ON public.parcel_detail (parcel_id);

CREATE INDEX IF NOT EXISTS parcel_detail_first_last_year_idx ON public.parcel_detail (first_year, last_year);

CREATE INDEX IF NOT EXISTS parcel_detail_class_code_idx ON public.parcel_detail (class_code)
WHERE
    class_code IS NOT NULL;

CREATE TABLE
    IF NOT EXISTS public.parcel_deeds (
        id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        parcel_id bigint NOT NULL,
        deed_book text,
        deed_page text,
        deed_document text,
        deed_date date NOT NULL,
        first_year integer NOT NULL,
        last_year integer NOT NULL,
        created_at timestamptz NOT NULL DEFAULT now (),
        updated_at timestamptz NOT NULL DEFAULT now (),
        CONSTRAINT parcel_deeds_year_range_check CHECK (first_year <= last_year)
    );

COMMENT ON TABLE public.parcel_deeds IS 'Parcel deed history grouped by deed identifiers with first/last year coverage.';

CREATE INDEX IF NOT EXISTS parcel_deeds_parcel_id_idx ON public.parcel_deeds (parcel_id);

CREATE INDEX IF NOT EXISTS parcel_deeds_deed_date_idx ON public.parcel_deeds (deed_date);

CREATE INDEX IF NOT EXISTS parcel_deeds_first_last_year_idx ON public.parcel_deeds (first_year, last_year);

