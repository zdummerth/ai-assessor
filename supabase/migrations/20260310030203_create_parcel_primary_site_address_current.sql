CREATE TABLE
	IF NOT EXISTS public.parcel_primary_site_address_current (
		id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
		parcel_id bigint NOT NULL,
		address_id text NOT NULL,
		devnet_address text,
		tiger_address text,
		geom geometry (Point, 4326),
		created_at timestamptz NOT NULL DEFAULT now (),
		updated_at timestamptz NOT NULL DEFAULT now (),
		CONSTRAINT parcel_primary_site_address_current_parcel_unique UNIQUE (parcel_id)
	);

COMMENT ON TABLE public.parcel_primary_site_address_current IS 'Current primary site address per parcel, joined from parcel/site mapping and address lookup.';

COMMENT ON COLUMN public.parcel_primary_site_address_current.devnet_address IS 'Source devnet full address text from parcel mapping export.';

COMMENT ON COLUMN public.parcel_primary_site_address_current.tiger_address IS 'Raw address text from tigerline/geocoder lookup.';

CREATE INDEX IF NOT EXISTS parcel_primary_site_address_current_parcel_id_idx ON public.parcel_primary_site_address_current (parcel_id);

CREATE INDEX IF NOT EXISTS parcel_primary_site_address_current_address_id_idx ON public.parcel_primary_site_address_current (address_id);

CREATE INDEX IF NOT EXISTS parcel_primary_site_address_current_geom_idx ON public.parcel_primary_site_address_current USING GIST (geom)
WHERE
	geom IS NOT NULL;

