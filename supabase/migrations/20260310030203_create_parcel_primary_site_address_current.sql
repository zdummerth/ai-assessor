CREATE POLICY parcel_primary_site_address_current_select_policy ON public.parcel_primary_site_address_current FOR
SELECT
	TO authenticated USING (
		public.authorize ('parcel_primary_site_address_current.read')
	);

CREATE POLICY parcel_primary_site_address_current_insert_policy ON public.parcel_primary_site_address_current FOR INSERT TO authenticated
WITH
	CHECK (
		public.authorize ('parcel_primary_site_address_current.write')
	);

CREATE POLICY parcel_primary_site_address_current_update_policy ON public.parcel_primary_site_address_current FOR
UPDATE TO authenticated USING (
	public.authorize ('parcel_primary_site_address_current.write')
)
WITH
	CHECK (
		public.authorize ('parcel_primary_site_address_current.write')
	);

CREATE POLICY parcel_primary_site_address_current_delete_policy ON public.parcel_primary_site_address_current FOR DELETE TO authenticated USING (
	public.authorize ('parcel_primary_site_address_current.delete')
);

CREATE OR REPLACE FUNCTION public.update_parcel_primary_site_address_current_updated_at()
RETURNS TRIGGER AS $$
BEGIN
	NEW.updated_at = now();
	RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER update_parcel_primary_site_address_current_updated_at
	BEFORE UPDATE ON public.parcel_primary_site_address_current
	FOR EACH ROW
	EXECUTE FUNCTION public.update_parcel_primary_site_address_current_updated_at();
