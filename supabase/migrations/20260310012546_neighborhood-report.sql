CREATE TABLE
    IF NOT EXISTS public.neighborhood_report (
        id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        parcel_id bigint NOT NULL,
        year integer NOT NULL,
        report_timestamp timestamptz NOT NULL,
        neighborhood text,
        address text,
        grade text,
        story text,
        occupancy text,
        cost_group text,
        total_area integer,
        gla integer,
        year_built integer,
        cdu text,
        ea text,
        funct text,
        econ text,
        land integer,
        improve integer,
        prior integer,
        percent_change numeric,
        bldg_source text
    );

COMMENT ON TABLE public.neighborhood_report IS 'Neighborhood report records used for appraisal analysis and review.';

CREATE INDEX IF NOT EXISTS neighborhood_report_parcel_id_idx ON public.neighborhood_report (parcel_id);

CREATE INDEX IF NOT EXISTS neighborhood_report_year_idx ON public.neighborhood_report (year);

CREATE INDEX IF NOT EXISTS neighborhood_report_report_timestamp_idx ON public.neighborhood_report (report_timestamp DESC);

CREATE INDEX IF NOT EXISTS neighborhood_report_neighborhood_idx ON public.neighborhood_report (neighborhood);

CREATE INDEX IF NOT EXISTS neighborhood_report_parcel_year_idx ON public.neighborhood_report (parcel_id, year);

