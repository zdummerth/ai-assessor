CREATE POLICY neighborhood_report_select_policy ON public.neighborhood_report FOR
SELECT
    TO authenticated USING (
        auth.role() = 'service_role'
        OR public.authorize('neighborhood_report.read')
    );

CREATE POLICY neighborhood_report_insert_policy ON public.neighborhood_report FOR INSERT TO authenticated
WITH
    CHECK (
        auth.role() = 'service_role'
        OR public.authorize('neighborhood_report.write')
    );

CREATE POLICY neighborhood_report_update_policy ON public.neighborhood_report FOR
UPDATE TO authenticated USING (
    auth.role() = 'service_role'
    OR public.authorize('neighborhood_report.write')
)
WITH
    CHECK (
        auth.role() = 'service_role'
        OR public.authorize('neighborhood_report.write')
    );

CREATE POLICY neighborhood_report_delete_policy ON public.neighborhood_report FOR DELETE TO authenticated USING (
    auth.role() = 'service_role'
    OR public.authorize('neighborhood_report.delete')
);

CREATE OR REPLACE FUNCTION public.update_neighborhood_report_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER update_neighborhood_report_updated_at
    BEFORE UPDATE ON public.neighborhood_report
    FOR EACH ROW
    EXECUTE FUNCTION public.update_neighborhood_report_updated_at();