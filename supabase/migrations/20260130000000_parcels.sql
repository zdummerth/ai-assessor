-- Row Level Security for parcels with permission-based authorization
ALTER TABLE public.parcels ENABLE ROW LEVEL SECURITY;

-- Read: users can see parcels if they have parcels.read permission
CREATE POLICY parcels_select_authorized ON public.parcels FOR
SELECT
    TO authenticated USING (
        auth.role () = 'service_role'
        OR public.authorize ('parcels.read')
    );

-- Write: users can insert/update parcels if they have parcels.write permission
CREATE POLICY parcels_insert_authorized ON public.parcels FOR INSERT TO authenticated
WITH
    CHECK (
        auth.role () = 'service_role'
        OR public.authorize ('parcels.write')
    );

CREATE POLICY parcels_update_authorized ON public.parcels FOR
UPDATE TO authenticated USING (
    auth.role () = 'service_role'
    OR public.authorize ('parcels.write')
)
WITH
    CHECK (
        auth.role () = 'service_role'
        OR public.authorize ('parcels.write')
    );

-- Delete: users can delete parcels if they have parcels.delete permission
CREATE POLICY parcels_delete_authorized ON public.parcels FOR DELETE TO authenticated USING (
    auth.role () = 'service_role'
    OR public.authorize ('parcels.delete')
);

-- Grant minimal permissions per security-least-privilege best practice
GRANT USAGE ON SCHEMA public TO authenticated;

GRANT
SELECT
    ON public.parcels TO authenticated;