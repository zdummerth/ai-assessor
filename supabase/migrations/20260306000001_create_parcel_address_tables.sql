-- Enable RLS
ALTER TABLE public.parcel_addresses ENABLE ROW LEVEL SECURITY;

ALTER TABLE public.retired_parcel_addresses ENABLE ROW LEVEL SECURITY;

-- RLS policies: parcel_addresses
CREATE POLICY parcel_addresses_select_policy ON public.parcel_addresses FOR
SELECT
    TO authenticated USING (
        auth.role () = 'service_role'
        OR public.authorize ('parcel_addresses.read')
    );

CREATE POLICY parcel_addresses_insert_policy ON public.parcel_addresses FOR INSERT TO authenticated
WITH
    CHECK (
        auth.role () = 'service_role'
        OR public.authorize ('parcel_addresses.write')
    );

CREATE POLICY parcel_addresses_update_policy ON public.parcel_addresses FOR
UPDATE TO authenticated USING (
    auth.role () = 'service_role'
    OR public.authorize ('parcel_addresses.write')
)
WITH
    CHECK (
        auth.role () = 'service_role'
        OR public.authorize ('parcel_addresses.write')
    );

CREATE POLICY parcel_addresses_delete_policy ON public.parcel_addresses FOR DELETE TO authenticated USING (
    auth.role () = 'service_role'
    OR public.authorize ('parcel_addresses.delete')
);

