-- Create parcel address relationship tables and seed permissions/RLS policies
CREATE TABLE
    IF NOT EXISTS public.parcel_addresses (
        id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        parcel_id bigint NOT NULL REFERENCES public.parcels (id) ON DELETE CASCADE,
        address_id text NOT NULL REFERENCES public.addresses_lookup_standardized (address_id) ON DELETE CASCADE,
        unit text,
        is_primary boolean NOT NULL DEFAULT false,
        created_at timestamptz NOT NULL DEFAULT now (),
        updated_at timestamptz NOT NULL DEFAULT now ()
    );

COMMENT ON TABLE public.parcel_addresses IS 'Active parcel-to-address relationships.';

COMMENT ON COLUMN public.parcel_addresses.parcel_id IS 'References parcels.id';

COMMENT ON COLUMN public.parcel_addresses.address_id IS 'References addresses_lookup_standardized.address_id';

COMMENT ON COLUMN public.parcel_addresses.unit IS 'Optional unit/suite for parcel-address mapping.';

COMMENT ON COLUMN public.parcel_addresses.is_primary IS 'Marks the primary address for the parcel.';

-- One primary address per parcel
CREATE UNIQUE INDEX IF NOT EXISTS parcel_addresses_one_primary_idx ON public.parcel_addresses (parcel_id)
WHERE
    is_primary = true;

CREATE INDEX IF NOT EXISTS parcel_addresses_parcel_id_idx ON public.parcel_addresses (parcel_id);

CREATE INDEX IF NOT EXISTS parcel_addresses_address_id_idx ON public.parcel_addresses (address_id);

CREATE TABLE
    IF NOT EXISTS public.retired_parcel_addresses (
        id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        parcel_id bigint NOT NULL REFERENCES public.parcels (id) ON DELETE CASCADE,
        address_id text NOT NULL REFERENCES public.addresses_lookup_standardized (address_id) ON DELETE CASCADE,
        unit text,
        is_primary boolean NOT NULL DEFAULT false,
        last_year integer NOT NULL,
        created_at timestamptz NOT NULL DEFAULT now (),
        updated_at timestamptz NOT NULL DEFAULT now ()
    );

COMMENT ON TABLE public.retired_parcel_addresses IS 'Historical parcel-to-address relationships for retired parcel records.';

COMMENT ON COLUMN public.retired_parcel_addresses.last_year IS 'Last calendar year this parcel-address relationship was active.';

-- One primary retired address per parcel per year
CREATE UNIQUE INDEX IF NOT EXISTS retired_parcel_addresses_one_primary_idx ON public.retired_parcel_addresses (parcel_id, last_year)
WHERE
    is_primary = true;

CREATE INDEX IF NOT EXISTS retired_parcel_addresses_parcel_id_idx ON public.retired_parcel_addresses (parcel_id);

CREATE INDEX IF NOT EXISTS retired_parcel_addresses_address_id_idx ON public.retired_parcel_addresses (address_id);

CREATE INDEX IF NOT EXISTS retired_parcel_addresses_last_year_idx ON public.retired_parcel_addresses (last_year);

