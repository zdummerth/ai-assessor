CREATE TABLE
	IF NOT EXISTS public.parcel_primary_site_address_history (
		id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
		parcel_id bigint NOT NULL,
		address_id text NOT NULL,
		devnet_address text,
		tiger_address text,
		geom geometry (Point, 4326),
		last_year integer NOT NULL,
		created_at timestamptz NOT NULL DEFAULT now (),
		updated_at timestamptz NOT NULL DEFAULT now (),
		CONSTRAINT parcel_primary_site_address_history_parcel_last_year_unique UNIQUE (parcel_id, last_year)
	);

COMMENT ON TABLE public.parcel_primary_site_address_history IS 'Historical primary site address rows per parcel by last active year.';

COMMENT ON COLUMN public.parcel_primary_site_address_history.devnet_address IS 'Source devnet full address text from parcel mapping export.';

COMMENT ON COLUMN public.parcel_primary_site_address_history.tiger_address IS 'Raw address text from tigerline/geocoder lookup.';

COMMENT ON COLUMN public.parcel_primary_site_address_history.last_year IS 'Last calendar year this primary parcel site address row was active.';

CREATE INDEX IF NOT EXISTS parcel_primary_site_address_history_parcel_id_idx ON public.parcel_primary_site_address_history (parcel_id);

CREATE INDEX IF NOT EXISTS parcel_primary_site_address_history_address_id_idx ON public.parcel_primary_site_address_history (address_id);

CREATE INDEX IF NOT EXISTS parcel_primary_site_address_history_last_year_idx ON public.parcel_primary_site_address_history (last_year);

CREATE INDEX IF NOT EXISTS parcel_primary_site_address_history_geom_idx ON public.parcel_primary_site_address_history USING GIST (geom)
WHERE
	geom IS NOT NULL;

