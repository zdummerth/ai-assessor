CREATE POLICY parcel_primary_site_address_history_select_policy ON public.parcel_primary_site_address_history FOR
SELECT
	TO authenticated USING (
		public.authorize ('parcel_primary_site_address_history.read')
	);

CREATE POLICY parcel_primary_site_address_history_insert_policy ON public.parcel_primary_site_address_history FOR INSERT TO authenticated
WITH
	CHECK (
		public.authorize ('parcel_primary_site_address_history.write')
	);

CREATE POLICY parcel_primary_site_address_history_update_policy ON public.parcel_primary_site_address_history FOR
UPDATE TO authenticated USING (
	public.authorize ('parcel_primary_site_address_history.write')
)
WITH
	CHECK (
		public.authorize ('parcel_primary_site_address_history.write')
	);

CREATE POLICY parcel_primary_site_address_history_delete_policy ON public.parcel_primary_site_address_history FOR DELETE TO authenticated USING (
	public.authorize ('parcel_primary_site_address_history.delete')
);

CREATE OR REPLACE FUNCTION public.update_parcel_primary_site_address_history_updated_at()
RETURNS TRIGGER AS $$
BEGIN
	NEW.updated_at = now();
	RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER update_parcel_primary_site_address_history_updated_at
	BEFORE UPDATE ON public.parcel_primary_site_address_history
	FOR EACH ROW
	EXECUTE FUNCTION public.update_parcel_primary_site_address_history_updated_at();
